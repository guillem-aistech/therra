/** Axis-aligned rectangle polygon around a centre (degrees half-extent). */
export function rectPolygon(
	lon: number,
	lat: number,
	half: [number, number],
): GeoJSON.Polygon {
	const [hx, hy] = half
	return {
		type: 'Polygon',
		coordinates: [
			[
				[lon - hx, lat - hy],
				[lon + hx, lat - hy],
				[lon + hx, lat + hy],
				[lon - hx, lat + hy],
				[lon - hx, lat - hy],
			],
		],
	}
}

export function lineString(coords: [number, number][]): GeoJSON.LineString {
	return { type: 'LineString', coordinates: coords }
}

